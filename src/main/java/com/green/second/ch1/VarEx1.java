package com.green.second.ch1;

public class VarEx1 {
    public static void main(String[] args) {
        // primitive type - 8개
        // 정수
        byte v1;
        short v2;
        long v3;
        int v5;

        // 실수
        double v4;
        float v7;

        // 논리형(불린형)
        boolean v6;
        // 문자형
        char v8;

        // reference type
        // 주소값을 저장할 수 있는 변수
        String str;

        // 변수의 값을 변경할 때는 `-` 대입 연산자를 사용한다.
        v5 = 5;
        v5 = 15;
        v5 = 10 + 10; // `+`가 있기 때문에 연산이 먼저 실행된다.
    }
}
